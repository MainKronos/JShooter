DROP DATABASE IF EXISTS JShooter;
CREATE DATABASE JShooter;
USE JShooter;



CREATE TABLE User(
	username VARCHAR(255) PRIMARY KEY,
	pass VARCHAR(255) NOT NULL,
	registration DATETIME NOT NULL
);

CREATE TABLE Map(
	mapID INT AUTO_INCREMENT PRIMARY KEY,
	map JSON NOT NULL
);

CREATE TABLE Score(
	scoreID INTEGER AUTO_INCREMENT PRIMARY KEY ,
	username VARCHAR(255),
	score INTEGER,
	dscore DATETIME,
	FOREIGN KEY (username) REFERENCES User(username) ON DELETE CASCADE
);

-- PROCEDURE -----------------------------------------------

DELIMITER $$
CREATE PROCEDURE UpdateScore(
IN _username VARCHAR(255), IN _score INTEGER, IN _dscore DATETIME
) BEGIN
	INSERT INTO Score(username, score, dscore) VALUE
	(_username, _score, _dscore);
END $$
DELIMITER ;

DROP PROCEDURE IF EXISTS GetScore;
DELIMITER $$
CREATE PROCEDURE GetScore(
	IN _page INTEGER, IN _size INTEGER
) 
BEGIN

	DECLARE offset INTEGER DEFAULT 0;

	SET offset = _page*_size;
	SET @pos = 0;


	SELECT p.*
	FROM (
		SELECT 
			k.username, 
			ROUND(k.score,0) as score, 
			k.matches,
			(@pos:=@pos+1) as rank
		FROM (
			SELECT 
				u.username, 
				u.registration, 
				IF(ISNULL(s.score), 0, (3*POW(10,14))/POW(s.score/1000, 6)+LOG10(s.matches+10)*10) as score, 
				IFNULL(s.matches,0) as matches
			FROM 
				User u LEFT OUTER JOIN (
					SELECT s.username, MIN(s.score) as score, COUNT(*) as matches
					FROM Score s
					GROUP BY s.username
				) as s on u.username = s.username
		)as k
		ORDER BY 
			k.score DESC, 
			k.registration ASC
	) as p
	LIMIT offset, _size;
END $$
DELIMITER ;


