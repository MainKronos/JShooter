DROP DATABASE IF EXISTS JShooter;
CREATE DATABASE JShooter;
USE JShooter;



CREATE TABLE User(
    username VARCHAR(255) PRIMARY KEY,
    pass VARCHAR(255) NOT NULL,
	registration DATETIME NOT NULL
);

CREATE TABLE Map(
    mapID VARCHAR(255) PRIMARY KEY,
    map JSON NOT NULL
);

CREATE TABLE Score(
	scoreID INTEGER AUTO_INCREMENT PRIMARY KEY ,
	username VARCHAR(255),
	mapID VARCHAR(255),
	score INTEGER,
	FOREIGN KEY (username) REFERENCES User(username) ON DELETE CASCADE,
	FOREIGN KEY (mapID) REFERENCES Map(mapID) ON DELETE CASCADE
);

-- PROCEDURE -----------------------------------------------

DELIMITER $$
CREATE PROCEDURE UpdateScore(
IN _username VARCHAR(255), IN _mapID VARCHAR(255), IN _score INTEGER
) BEGIN
    INSERT INTO Score(username, mapID, score) VALUE
    (_username, _mapID, _score);
END $$
DELIMITER ;

DELIMITER $$
CREATE PROCEDURE GetScore() 
BEGIN
    SELECT d.username, d.score, d.matches
	FROM (
		SELECT u.username, ROUND(AVG(s.score),0) as score, SUM(IFNULL(s.matches,0)) as matches, u.registration
		FROM User u
			LEFT OUTER JOIN (
				SELECT s.username, s.mapID, MIN(s.score) as score, COUNT(*) as matches
				FROM score s
				GROUP BY s.username, s.mapID
			) as s on u.username = s.username
		GROUP BY u.username
	) as d
	ORDER BY ISNULL(d.score), d.score, d.registration;
END $$
DELIMITER ;


