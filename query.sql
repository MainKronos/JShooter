DROP DATABASE IF EXISTS JShooter;
CREATE DATABASE JShooter;
USE JShooter;



CREATE TABLE User(
    username VARCHAR(255) PRIMARY KEY,
    pass VARCHAR(255) NOT NULL,
	registration DATETIME NOT NULL
);

CREATE TABLE Map(
    mapID INT AUTO_INCREMENT PRIMARY KEY,
    map JSON NOT NULL
);

CREATE TABLE Score(
	scoreID INTEGER AUTO_INCREMENT PRIMARY KEY ,
	username VARCHAR(255),
	mapID int,
	score INTEGER,
	FOREIGN KEY (username) REFERENCES User(username) ON DELETE CASCADE,
	FOREIGN KEY (mapID) REFERENCES Map(mapID) ON DELETE CASCADE
);

-- PROCEDURE -----------------------------------------------

DELIMITER $$
CREATE PROCEDURE UpdateScore(
IN _username VARCHAR(255), IN _mapID VARCHAR(255), IN _score INTEGER
) BEGIN
    INSERT INTO Score(username, mapID, score) VALUE
    (_username, _mapID, _score);
END $$
DELIMITER ;

DELIMITER $$
CREATE PROCEDURE GetScore() 
BEGIN
	SELECT 
		k.username, 
		ROUND(k.score,0) as score, 
		k.matches
	FROM (
		SELECT 
			u.username, 
			u.registration, 
			IF(ISNULL(s.score), NULL, (10000000/s.score)+LOG10(s.matches+10)*10) as score, 
			IFNULL(s.matches,0) as matches
		FROM 
			User u LEFT OUTER JOIN (
				SELECT s.username, MIN(s.score) as score, COUNT(*) as matches
				FROM Score s
				GROUP BY s.username
			) as s on u.username = s.username
	)as k
	ORDER BY 
		k.score DESC, 
		k.registration ASC;
END $$
DELIMITER ;


